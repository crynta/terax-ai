function interpolate(text: string, vars?: Vars): string {
  if (!vars) return text;
  return text.replace(/\{\{(\w+)\}\}/g, (_, key: string) =>
    String(vars[key] ?? ""),
  );
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<LanguagePref>(
    DEFAULT_PREFERENCES.language,
  );

  useEffect(() => {
    let alive = true;
    void loadPreferences().then((prefs) => {
      if (alive) setLanguageState(prefs.language);
    });
    const unlistenP = onPreferencesChange((key, value) => {
      if (key === "language" && (value === "en" || value === "zh-CN")) {
        setLanguageState(value);
      }
    });
    return () => {
      alive = false;
      void unlistenP.then((fn) => fn());
    };
  }, []);

  const setLanguage = useCallback((next: LanguagePref) => {
    setLanguageState(next);
    void persistLanguage(next);
  }, []);

  const t = useCallback(
    (key: string, vars?: Vars) => {
      const translated = language === "zh-CN" ? ZH_CN[key] : undefined;
      return interpolate(translated ?? key, vars);
    },
    [language],
  );

  const value = useMemo(
    () => ({ language, setLanguage, t }),
    [language, setLanguage, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within an <I18nProvider>");
  return ctx;
}
